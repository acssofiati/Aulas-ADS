# Elabore um programa que calcule e apresente o volume de uma caixa retangular,
# por meio da fórmula VOLUME = COMPRIMENTO * LARGURA * ALTURA.

com = float(input("Digite o valor do Comprimento da Caixa: "))
lar = float(input("Digite o valor da Largura da Caixa: "))
alt = float(input("Digite o valor da Altura da Caixa: "))

vol = com * lar * alt

print (f"O volume total da caixa é de {vol}.")